// Converter Formula
float mmoltomg(float a) {
	
	return (a * 18);	
}

// Total Cholesterol Formula
float tot_chol(float a, float b, float c) {
	
	return (a + b + (0.2 * c));
}

// Ratio Formula
float ratio(float a, float b) {
	
	return (a / b);
}